/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	 HAL_Init();
	    SystemClock_Config();

	    GPIO_InitTypeDef GPIO_InitStruct;
	    __HAL_RCC_GPIOA_CLK_ENABLE();
	    __HAL_RCC_GPIOC_CLK_ENABLE();

	    // Configure LED pin
	    GPIO_InitStruct.Pin = GPIO_PIN_5;
	    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
	    GPIO_InitStruct.Pull = GPIO_NOPULL;
	    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
	    HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

	    // Configure button pin
	    GPIO_InitStruct.Pin = GPIO_PIN_13; // Button connected to pin PC13
	    GPIO_InitStruct.Mode = GPIO_MODE_INPUT;
	    GPIO_InitStruct.Pull = GPIO_PULLUP; // Button pull-up configuration
	    HAL_GPIO_Init(GPIOC, &GPIO_InitStruct);

	    StateMachine machine;
	    machine.currentState = LED_OFF_STATE; // Initialize state machine

	    // Main event loop
	    while (1) {
	        // Check if the button is pressed (assuming button press corresponds to low level)
	        if (HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == GPIO_PIN_RESET) {
	            // Button is pressed, dispatch the BUTTON_PRESSED_EVENT
	            dispatch(&machine, BUTTON_PRESSED_EVENT);
	            // Delay to debounce button press
	            HAL_Delay(1000);
	        }
	    }

	    return 0;
	}
